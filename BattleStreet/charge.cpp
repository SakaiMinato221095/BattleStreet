//-------------------------------------
//- タイマーの初期化処理
//-------------------------------------
HRESULT CCharge::Init(void)
{
	// 親クラスの初期化処理
	if (CAttack::Init() == E_FAIL)
	{
		return E_FAIL;
	}

	// 成功を返す
	return S_OK;
}

//-------------------------------------
//- タイマーの終了処理
//-------------------------------------
void CCharge::Uninit(void)
{
	// 親クラスの終了処理
	CAttack::Uninit();
}

